/* Квадрат числа
Напиши программу, которая выводит на экран квадрат числа 5
*/
public class Solution
{
    public static void main(String[] args)
    {
        System.out.println(sqr(5));
    }

    public static int sqr(int a)
    {
        return a * a;
    }
}

